create table public.instagram_prospects_cache (
  id integer generated by default as identity not null,
  username text not null,
  full_name text null,
  title text null,
  location text null,
  profile_url text null,
  photo_url text null,
  biography text null,
  keyword_match text null,
  comment text null,
  comment_date timestamp without time zone null,
  post_url text null,
  qualified boolean null default false,
  processed boolean null default false,
  cached_at text null,
  processed_at text null,
  constraint Instagram Prospects Cache_pkey primary key (id),
  constraint Instagram Prospects Cache_username_key unique (username)
) TABLESPACE pg_default;